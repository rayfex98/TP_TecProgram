*/
CREATE PROCEDURE DETALLEPROC	(@ID SMALLINT,
								@ORDEN INT,
								@PRODUCTO INT,
								@CANTIDAD SMALLINT,
								@TIPO NVARCHAR(10) = '')
AS
	BEGIN
