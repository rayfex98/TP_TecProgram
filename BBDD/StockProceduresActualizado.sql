					   @ORDEN,
					   @CANTIDAD)
			END
		IF @TIPO = 'SELECT'
			BEGIN
				SELECT	[IDDETALLE],
						[IDORDEN],
						[IDPRODUCTO],
						[CANTIDAD]
				FROM [dbo].[DETALLEORDEN]
			END
		IF @TIPO = 'UPDATE' /*No deberia poder asociar detalle a otro idOrden*/
			BEGIN
				IF (@PRODUCTO IS NOT NULL)
