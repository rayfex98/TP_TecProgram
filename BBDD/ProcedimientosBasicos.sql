/*procedimientos del DETALLEORDEN, EXEC DETALLEPROC @ID = SMALLINT,@ORDEN=INT,@PRODUCTO=INT,@CANTIDAD=SMALLINT,@TIPO = '';*/
CREATE PROCEDURE DETALLEPROC	(@ID SMALLINT,
								@ORDEN INT,
								@PRODUCTO INT,
								@CANTIDAD SMALLINT,
								@TIPO NVARCHAR(10) = '')
AS
	BEGIN
		IF @TIPO = 'INSERT'
			BEGIN
				INSERT INTO [dbo].[DETALLEORDEN]
					   ([IDPRODUCTO],
					   [CANTIDAD])
				VALUES
					   (@PRODUCTO,
					   @CANTIDAD)
			END
		IF @TIPO = 'SELECTALL'
			BEGIN
				SELECT [IDDETALLE],
						[IDORDEN],
						[IDPRODUCTO],
						[CANTIDAD]
				FROM [dbo].[DETALLEORDEN]
			END
		IF @TIPO = 'SELECTONE'
			BEGIN
				SELECT [IDDETALLE],
						[IDORDEN],
						[IDPRODUCTO],
						[CANTIDAD]
				FROM [dbo].[DETALLEORDEN]
				WHERE (([IDORDEN] = @ORDEN AND [IDDETALLE] = @ID) OR [IDORDEN] = @ORDEN)
			END
		IF @TIPO = 'UPDATE'
			BEGIN
				IF (@PRODUCTO IS NOT NULL)
					BEGIN
						UPDATE [dbo].[DETALLEORDEN]
						SET	[IDPRODUCTO] = @PRODUCTO
						WHERE [IDDETALLE] = @ID
					END
				IF (@CANTIDAD IS NOT NULL)
					BEGIN
						UPDATE [dbo].[DETALLEORDEN]
						SET	[CANTIDAD] = @CANTIDAD
						WHERE [IDDETALLE] = @ID
					END
			END
		IF @TIPO = 'DELETE'
			BEGIN
				DELETE FROM [dbo].[DETALLEORDEN]
				WHERE (([IDORDEN] = @ORDEN AND [IDDETALLE] = @ID) OR [IDORDEN] = @ORDEN)
			END
	END
GO