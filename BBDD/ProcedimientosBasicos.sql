/*procedimientos del ALERTA, EXEC ALERTAPROC @ID = INT,@STOCK = INT,@USUARIO = INT,@MINIMO = INT,@TIPO = NVARCHAR(10);*/ 
CREATE PROCEDURE ALERTAPROC	(@ID INT,
							@STOCK INT,
							@USUARIO INT,
							@MINIMO INT,
							@TIPO NVARCHAR(10) = '')
AS
	BEGIN
		IF @TIPO = 'INSERT'
			BEGIN
				INSERT INTO [dbo].[ALERTA]
					   ([IDSTOCK],
					   [IDPERSONA],
					   [CANTIDADMINIMA])
				VALUES
					   (@STOCK,
					   @USUARIO,
					   @MINIMO)
			END
		IF @TIPO = 'SELECTALL'
			BEGIN
				SELECT [IDALERTA],
						[IDSTOCK],
						[IDPERSONA],
						[CANTIDADMINIMA]
				FROM [dbo].[ALERTA]
			END
		IF @TIPO = 'SELECTONE'
			BEGIN
				SELECT [IDALERTA],
						[IDSTOCK],
					   [IDPERSONA],
					   [CANTIDADMINIMA]
				FROM [dbo].[ALERTA]
				WHERE ([IDALERTA] = @ID OR [IDSTOCK] = @STOCK OR [IDPERSONA] = @USUARIO) 
			END
		IF @TIPO = 'UPDATE'
			BEGIN
				IF (@STOCK IS NOT NULL) 
					BEGIN
						UPDATE [dbo].[ALERTA]
						SET [IDSTOCK] = @STOCK
						WHERE [IDALERTA] = @ID
					END
				IF (@USUARIO IS NOT NULL) 
					BEGIN
						UPDATE [dbo].[ALERTA]
						SET	[IDPERSONA] = @USUARIO
						WHERE [IDALERTA] = @ID
					END
				IF (@MINIMO IS NOT NULL) 
					BEGIN
						UPDATE [dbo].[ALERTA]
						SET	[CANTIDADMINIMA] = @MINIMO
						WHERE [IDALERTA] = @ID
					END
				
			END
		IF @TIPO = 'DELETE'
			BEGIN
				DELETE FROM [dbo].[ALERTA]
				WHERE [IDALERTA] = @ID
			END
	END
GO
/*procedimientos del CATEGORIA, EXEC CATEGORIAPROC @ID = INT,@DESCRIPCION = VARCHAR(30),@TIPO = NVARCHAR(10);*/
CREATE PROCEDURE CATEGORIAPROC (@ID INT,
								@DESCRIPCION VARCHAR(30),
								@TIPO NVARCHAR(10) = '')
AS
	BEGIN
		IF @TIPO = 'INSERT'
			BEGIN
				INSERT INTO [dbo].[CATEGORIA]
					   ([DESCRIPCION])
				VALUES
					   (@DESCRIPCION)
			END
		IF @TIPO = 'SELECTALL'
			BEGIN
				SELECT [IDCATEGORIA],
						[DESCRIPCION]
				FROM [dbo].[CATEGORIA]
			END
		IF @TIPO = 'SELECTONE'
			BEGIN
				SELECT	[IDCATEGORIA],
						[DESCRIPCION]
				FROM [dbo].[CATEGORIA]
				WHERE [IDCATEGORIA] = @ID
			END
		IF @TIPO = 'UPDATE'
			BEGIN
				IF (@DESCRIPCION IS NOT NULL)
					BEGIN
						UPDATE [dbo].[CATEGORIA]
						SET	[DESCRIPCION] = @DESCRIPCION
						WHERE [IDCATEGORIA] = @ID
					END
			END
		IF @TIPO = 'DELETE'
			BEGIN
				DELETE FROM [dbo].[CATEGORIA]
				WHERE [IDCATEGORIA] = @ID
			END
	END
GO
